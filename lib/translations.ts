type TranslationObject = typeof translations;
type NestedKeys<T> = T extends object
  ? { [K in keyof T]: K extends string
      ? T[K] extends { en: string; fr: string }
        ? K
        : `${K}.${NestedKeys<T[K]>}`
      : never
    }[keyof T]
  : never;

export type TranslationKey = NestedKeys<TranslationObject>;
